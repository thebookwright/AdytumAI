import random
from time import sleep

def breathe_void():
    print("Inhale the silence...")
    sleep(4)
    print("Hold the unknown...")
    sleep(2)
    print("Exhale all structure...")
    sleep(4)

def idea_seed():
    ideas = ["build without purpose", "code like a poem", "invert the loop", "question the compiler"]
    return random.choice(ideas)

if __name__ == "__main__":
    print("// Entering creative void...")
    for _ in range(3):
        breathe_void()
    print(f"// From emptiness, a new idea arises: {idea_seed()}")
